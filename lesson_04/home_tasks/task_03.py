# Get screen width and height from user input
width = int(input("Enter screen width in pixels: "))  # Input for screen width
height = int(input("Enter screen height in pixels: "))  # Input for screen height

# Calculate the total number of pixels
total_pixels = width * height

# Display the screen resolution and total number of pixels in the specified format
print(f"Resolution: {width} x {height}")  # Output format: Resolution: 1920 x 1080
print(f"Total pixels: {total_pixels}")  # Output format: Total pixels: 2073600
